    static let cellTitleFont = UIFont.systemFont(ofSize: 24, weight: .bold)
    static let cellSubTitleFont = UIFont.systemFont(ofSize: 16, weight: .bold)
    static let bodyFont = UIFont.systemFont(ofSize: 16, weight: .regular)
}
